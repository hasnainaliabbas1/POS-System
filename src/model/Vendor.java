public class Vendor {
    private int id;
    private String name;
    private String contactInfo;
    private String address;

    // New fields for company details
    private String companyName;
    private String companyContactInfo;
    private String companyAddress;

    // Getter and Setter for id
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    // Getter and Setter for name
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    // Getter and Setter for contactInfo
    public String getContactInfo() {
        return contactInfo;
    }

    public void setContactInfo(String contactInfo) {
        this.contactInfo = contactInfo;
    }

    // Getter and Setter for address
    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    // Getter and Setter for companyName

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }


    public String getCompanyName() {
        return companyName;
    }



    // Getter and Setter for companyContactInfo
    public String getCompanyContactInfo() {
        return companyContactInfo;
    }

    public void setCompanyContactInfo(String companyContactInfo) {
        this.companyContactInfo = companyContactInfo;
    }

    // Getter and Setter for companyAddress
    public String getCompanyAddress() {
        return companyAddress;
    }

    public void setCompanyAddress(String companyAddress) {
        this.companyAddress = companyAddress;
    }
}
